# A tuple is an immutable(can’t change or modified) data type in Python.
# Once defined, tuple elements can’t be manipulated or altered or updated.

#Tuples are created using ()
t1 = () # example of empty tuple

# t2 = (1) #Wrong way to declare single element tuple this will give an error.

t2 = (1,) #tuples with only one element .

t3=(1,23,34,45,78) #tuples with more than one element.

