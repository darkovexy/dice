#we can slice List like a string in python.
#But here slicing is done between the set of items.
L1 = ["Biswa","Mriganka","Bhaskar","Sanjeev","Deepjyoti","47",False]

print(L1[:]) #it will print complete list.
print(L1[2:]) #slicing starts at index no.2 
print(L1[-4:]) #similar as [1:]
#print(L1[-2:-4]) #We can not print list in reverse order just using indexing
# print(L1[4:1])
print(L1[-5:-1])#same as [0:4]  